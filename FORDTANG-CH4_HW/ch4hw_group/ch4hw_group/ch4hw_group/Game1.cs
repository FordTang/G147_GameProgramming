    public class Game1 : Microsoft.Xna.Framework.Game
    {
        GraphicsDeviceManager graphics;
        SpriteBatch spriteBatch;
        Texture2D ak47;
        Texture2D colt;
        Texture2D revolver;
        Texture2D shotgun;
        Rectangle tex2Rect;
        Vector2 tex2Vec = new Vector2(0, 160);

